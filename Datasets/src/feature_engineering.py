import pandas as pd
import numpy as np


# Load CSV files
student_info = pd.read_csv("studentInfo.csv")
student_vle = pd.read_csv(
    "studentVle.csv",
    sep=",",
    engine="python",
        encoding="latin1",

    on_bad_lines="skip"
)
vle = pd.read_csv("vle.csv")
assessments = pd.read_csv("assessments.csv")
student_assessment = pd.read_csv("studentAssessment.csv")

print("Data Loaded Successfully")

"""Build Engagement Features (From studentVle)
studentVle contains:
id_student
date
sum_click
"""

all_clicks = (
    student_vle
    .groupby("id_student")["sum_click"]
    .sum()
    .reset_index()
    .rename(columns={"sum_click": "all_clicks"})
)

active_days = (
    student_vle
    .groupby("id_student")["date"]
    .nunique()
    .reset_index()
    .rename(columns={"date": "active_days"})
)

engagement = all_clicks.merge(active_days, on="id_student")
engagement["access_frequency"] = (
    engagement["all_clicks"] / engagement["active_days"]
)

student_vle_full = student_vle.merge(
    vle,
    on=["id_site", "code_module", "code_presentation"]
)

# Select material types only
material_types = ["resource", "page", "url"]

material_clicks = (
    student_vle_full[
        student_vle_full["activity_type"].isin(material_types)
    ]
    .groupby("id_student")["sum_click"]
    .sum()
    .reset_index()
    .rename(columns={"sum_click": "material_clicks"})
)

assessment_full = student_assessment.merge(
    assessments,
    on="id_assessment",
    how="left"
)

quiz_data = assessment_full[
    assessment_full["assessment_type"] == "CMA"
]

quiz_features = (
    quiz_data
    .groupby("id_student")
    .agg(
        avg_quiz_score=("score", "mean"),
        quiz_attempts=("score", "count")
    )
    .reset_index()
)

assignment_data = assessment_full[
    assessment_full["assessment_type"] == "TMA"
]

assignment_features = (
    assignment_data
    .groupby("id_student")
    .agg(
        avg_assignment_score=("score", "mean"),
        assignment_submissions=("score", "count")
    )
    .reset_index()
)

# Extract final result
final_grade = student_info[["id_student", "final_result"]].copy()

# Create numeric mapping
grade_mapping = {
    "Distinction": 85,
    "Pass": 65,
    "Fail": 40,
    "Withdrawn": 30
}

# Apply mapping
final_grade["final_grade"] = final_grade["final_result"].map(grade_mapping)

print(final_grade.head())

dataset = engagement.merge(material_clicks, on="id_student", how="left")
dataset = dataset.merge(quiz_features, on="id_student", how="left")
dataset = dataset.merge(assignment_features, on="id_student", how="left")
dataset = dataset.merge(final_grade[["id_student", "final_grade"]], on="id_student")

dataset.fillna(0, inplace=True)

dataset["risk_cluster"] = 0

dataset["total_time_spent"] = dataset["all_clicks"] * 2

dataset.rename(columns={"id_student": "student_id"}, inplace=True)

dataset.to_csv("academIQ_clean_dataset.csv", index=False)

print("AcademIQ dataset created successfully!")

